	private void jPanel1MouseReleased(java.awt.event.MouseEvent evt) {
		if (!townsSet) {
			Graphics gra = jPanel1.getGraphics();
			gra.setColor(Color.darkGray);
			gra.drawRect(evt.getX() - 3, evt.getY() - 3, 7, 7);
			gra.setColor(Color.CYAN);
			gra.fillRect(evt.getX() - 2, evt.getY() - 2, 6, 6);
			TownCluster.getInstance().getCluster()
					.add(new Town(evt.getX(), evt.getY()));
			jLabel11.setText(String.valueOf(Integer.parseInt(jLabel11.getText()) + 1));
		}
	}

	private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {
		JFileChooser fc = new JFileChooser();
		fc.setCurrentDirectory(new File("./res"));
		int returnVal = fc.showOpenDialog(TSP_GUI.this);
		if (returnVal == JFileChooser.APPROVE_OPTION) {
			XmlInputReader.getInstance().readXMLFile(fc.getSelectedFile());
			for (Position p : TownCluster.getInstance().getCluster()) {
				Graphics gra = jPanel1.getGraphics();
				gra.setColor(Color.darkGray);
				gra.drawRect(p.getPosition_x() - 3, p.getPosition_y() - 3, 7, 7);

				gra.setColor(Color.CYAN);
				gra.fillRect(p.getPosition_x() - 2, p.getPosition_y() - 2, 6, 6);

				jLabel11.setText(String.valueOf(TownCluster.getInstance()
						.getCluster().size()));
			}
		}

	}

