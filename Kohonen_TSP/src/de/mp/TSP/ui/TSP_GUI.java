	private void updateLayer() {
		Graphics gra = jPanel2.getGraphics();
		jPanel2.update(gra);
		for (Position p : TownCluster.getInstance().getCluster()) {
			gra.setColor(Color.ORANGE);
			gra.drawRect(p.getPosition_x() - 5, p.getPosition_y() - 5, 11, 11);
			gra.setColor(Color.MAGENTA);
			gra.fillRect(p.getPosition_x() - 2, p.getPosition_y() - 2, 6, 6);
			gra.setColor(Color.ORANGE);
			gra.fillRect(p.getPosition_x() - 1, p.getPosition_y() - 1, 4, 4);
		}
		for (Position p : NeuronCluster.getInstance().getCluster()) {
			Neuron n = (Neuron) p;
			gra.setColor(Color.GREEN);
			gra.drawRect(p.getPosition_x() - 2, p.getPosition_y() - 2, 4, 4);
			gra.setColor(Color.CYAN);
			gra.drawLine(n.getPosition_x(), n.getPosition_y(), n
					.getNeighbour_prev().getPosition_x(), n.getNeighbour_prev()
					.getPosition_y());
		}
	}

